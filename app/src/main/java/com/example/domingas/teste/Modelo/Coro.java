package com.example.domingas.teste.Modelo;

public class Coro {
    String uid;
    String numero;
    String cargo;
    String tipocoro;

    public Coro() {
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getNumero() {
        return numero;
    }

    public void setNumero(String numero) {
        this.numero = numero;
    }

    public String getCargo() {
        return cargo;
    }

    public void setCargo(String cargo) {
        this.cargo = cargo;
    }

    public String getTipocoro() {
        return tipocoro;
    }

    public void setTipocoro(String tipocoro) {
        this.tipocoro = tipocoro;
    }
}

